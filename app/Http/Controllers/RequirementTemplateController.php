        $validator = Validator::make($request->all(),[
            'title'   => ['required'],
            'financing_type'    => ['required']
        ]);

        if($validator->passes())
        {
           $template = new Template();
           $template->name = $request->title;
        }
        return response()->json($validator->errors());
